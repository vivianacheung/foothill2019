public class Draw_Cat {

	private static void draw_cat() {
		System.out.println("  -------");
		System.out.println(" | /\\_/\\ |");
		System.out.println(" |( o o )|");
		System.out.println(" | > ^ < |");
		System.out.println("  -------");
		System.out.println("Schrodinger");
	}

	public static void main(String[] args) {
		draw_cat();
	}

}
